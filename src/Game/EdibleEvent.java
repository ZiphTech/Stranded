package Game;

        /* 
         * event 0
         * **Edible event**
         * event 1 -- Berries
         * event 2 -- Mushrooms
         * event 3 -- Coconuts
         * event 4 -- Frogs
         * event 5 -- Turtles
         * default -- nothing
         */

public interface EdibleEvent 
{
    public int BERRIES = 1;
    public int MUSHROOMS = 2;
    public int COCONUTS = 3;
    public int FROGS = 4;
    public int TURTLES = 5;
}
